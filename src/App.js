function ListItemLink(props) {
  return <ListItem button component="a" {...props} />;
}

export default function PersistentDrawerLeft() {
  const classes = useStyles();
  const theme = useTheme();
  const [open, setOpen] = React.useState(false);

  const handleDrawerOpen = () => {
    setOpen(true);
  };

  const handleDrawerClose = () => {
    setOpen(false);
  };

  return (
    <Router>
      <div className={classes.root}>
        <CssBaseline />
        <AppBar
          position="fixed"
          className={clsx(classes.appBar, {
            [classes.appBarShift]: open
          })}
        >
          <Toolbar>
            <IconButton
              color="inherit"
              aria-label="open drawer"
              onClick={handleDrawerOpen}
              edge="start"
              className={clsx(classes.menuButton, open && classes.hide)}
            >
              <MenuIcon />
            </IconButton>
            <Typography variant="h6" noWrap>
              Art Scouters
            </Typography>
          </Toolbar>
        </AppBar>
        <Drawer
          className={classes.drawer}
          variant="persistent"
          anchor="left"
          open={open}
          classes={{
            paper: classes.drawerPaper
          }}
        >
          <div className={classes.drawerHeader}>
            <IconButton onClick={handleDrawerClose}>
              {theme.direction === "ltr" ? (
                <ChevronLeftIcon />
              ) : (
                <ChevronRightIcon />
              )}
            </IconButton>
          </div>
          <Divider />
          <List>
            {["Inbox", "Starred", "Send email", "Drafts"].map((text, index) => (
              <ListItem button key={text}>
                <ListItemIcon>
                  {index % 2 === 0 ? <InboxIcon /> : <MailIcon />}
                </ListItemIcon>
                <ListItemText primary={text} />
              </ListItem>
            ))}
          </List>
          <Divider />
          <List>
            {["List", "Trash", "Spam"].map((text, index) => (
              // <ListItem button key={text}>
              //   <ListItemText primary={text} />
              // </ListItem>
              <ListItemLink href={index % 2 === 0 ? "/viewArt_2" : "#"}>
                <ListItemIcon>
                  {index % 2 === 0 ? <ListIcon /> : <MailIcon />}
                </ListItemIcon>
                <ListItemText primary={text} />
              </ListItemLink>
            ))}
          </List>
        </Drawer>
        <main
          className={clsx(classes.content, {
            [classes.contentShift]: open
          })}
        >
          <div className={classes.drawerHeader} />
          <div>
            {/*
          A <Switch> looks through all its children <Route>
          elements and renders the first one whose path
          matches the current URL. Use a <Switch> any time
          you have multiple routes, but you want only one
          of them to render at a time
        */}
            <Switch>
              {/* <Route exact path="/">
                <Home />
              </Route> */}
              <Route path="/about">
                <About />
              </Route>
              <Route path="/dashboard">
                <Dashboard />
              </Route>
              <Routes />
            </Switch>
          </div>
          <Box
            display="flex"
            // flexDirection="row"
            flexDirection="column"
            p={0}
            m={0}
            bgcolor="white"
            color="white"
            sm={12}
          >
            <Box
              sm={12}
              p={1}
              mt={0}
              style={{ width: "100%" }}
              bgcolor="#ef744c"
            >
              <Typography variant="h3" display="block" gutterBottom>
                Footer 1
              </Typography>
            </Box>
            <Box p={1} mt={0} style={{ width: "100%" }} bgcolor="#cf5636">
              <Typography variant="caption" display="block" gutterBottom>
                Footer 2
              </Typography>
            </Box>
            <Box p={1} mt={"3px"} style={{ width: "100%" }} bgcolor="#4f4947">
              <MenuList style={{ display: "flex" }}>
                <MenuItem component={Link} to="/">
                  Home
                </MenuItem>
                <MenuItem component={Link} to="test">
                  Test
                </MenuItem>
                <MenuItem component={Link} to="/about">
                  About
                </MenuItem>
                <MenuItem component={Link} to="/dashboard">
                  Dashboard
                </MenuItem>
              </MenuList>
            </Box>
          </Box>
        </main>
      </div>
    </Router>
  );
}

